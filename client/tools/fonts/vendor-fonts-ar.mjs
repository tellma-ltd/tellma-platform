/**
 * Vendors the Noto Sans Arabic subset into @tellma/locale-ar (spec §7.1):
 * the locale pack ships its script's woff2 + @font-face (unicode-range) +
 * TM_FONT_SUBSETS entries — the template every later pack copies.
 *
 * Usage: node tools/fonts/vendor-fonts-ar.mjs
 */
import { createHash } from 'node:crypto';
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const clientDir = join(dirname(fileURLToPath(import.meta.url)), '..', '..');
const packDir = join(clientDir, 'projects', 'locale', 'tellma-locale-ar');
const fontsDir = join(packDir, 'fonts');
mkdirSync(fontsDir, { recursive: true });

// Generated files are committed, so they carry the license header too.
const LICENSE_SLASH = [
  '// Copyright (c) Tellma Ltd. All rights reserved.',
  '//',
  '// This source code is licensed under the Apache-2.0 license found in the',
  '// LICENSE file in the root directory of this source tree.',
  '',
].join('\n');
const LICENSE_CSS = [
  '/*',
  ' * Copyright (c) Tellma Ltd. All rights reserved.',
  ' *',
  ' * This source code is licensed under the Apache-2.0 license found in the',
  ' * LICENSE file in the root directory of this source tree.',
  ' */',
  '',
].join('\n');

const sourceDir = join(clientDir, 'node_modules', '@fontsource-variable', 'noto-sans-arabic');
const css = readFileSync(join(sourceDir, 'index.css'), 'utf8');

let file = '';
let unicodeRange = '';
for (const block of css.split('@font-face')) {
  const match = block.match(/files\/(noto-sans-arabic-arabic-wght-normal\.woff2)/);
  if (match) {
    file = match[1];
    unicodeRange = block.match(/unicode-range:\s*([^;]+);/)?.[1]?.trim() ?? '';
  }
}
if (!file || !unicodeRange) {
  throw new Error('Arabic subset not found in the Fontsource css');
}

// Content-hashed binary + a stylesheet whose URLs are relative to itself:
// both are served as static assets (fonts/arabic/ in the consuming app),
// never through the styles pipeline, so the manifest URL below and the
// @font-face URL resolve to the same immutable file.
const bytes = readFileSync(join(sourceDir, 'files', file));
const hash = createHash('sha256').update(bytes).digest('hex').slice(0, 10);
const hashedFile = file.replace(/\.woff2$/, `.${hash}.woff2`);
writeFileSync(join(fontsDir, hashedFile), bytes);

writeFileSync(
  join(fontsDir, 'fonts.css'),
  [
    LICENSE_CSS,
    '/* GENERATED by tools/fonts/vendor-fonts-ar.mjs — do not edit by hand.',
    '   Self-hosted Noto Sans Arabic (SIL OFL 1.1 — see OFL.txt); fetched on',
    '   demand via unicode-range only when Arabic glyphs render. */',
    '',
    '@font-face {',
    "  font-family: 'Noto Sans Arabic';",
    '  font-style: normal;',
    '  font-display: swap;',
    '  font-weight: 100 900;',
    `  src: url(./${hashedFile}) format('woff2-variations');`,
    `  unicode-range: ${unicodeRange};`,
    '}',
    '',
  ].join('\n'),
);

const license = readFileSync(join(sourceDir, 'LICENSE'), 'utf8');
writeFileSync(
  join(fontsDir, 'OFL.txt'),
  'Noto Sans Arabic — SIL Open Font License 1.1\n' +
    'Vendored from the Fontsource package (fontsource.org).\n\n' +
    license,
);

writeFileSync(
  join(packDir, 'font-manifest.generated.ts'),
  [
    LICENSE_SLASH,
    '// GENERATED by tools/fonts/vendor-fonts-ar.mjs — do not edit by hand.',
    "import type { TmFontSubset } from '@tellma/core-ui';",
    '',
    '/** The Arabic subset shipped in @tellma/locale-ar. */',
    'export const TM_FONTS_ARABIC: readonly TmFontSubset[] = [',
    '  {',
    "    family: 'Noto Sans Arabic',",
    "    script: 'arabic',",
    "    style: 'normal',",
    "    weight: '100 900',",
    `    url: 'fonts/arabic/${hashedFile}',`,
    `    unicodeRange: '${unicodeRange}',`,
    '  },',
    '];',
    '',
  ].join('\n'),
);

console.log(`vendored Arabic subset -> ${fontsDir}`);
